package kyo

import scala.scalajs.js
import scala.scalajs.js.annotation.*

@JSExportTopLevel("HttpMissingBoundaryException")
class JsHttpMissingBoundaryException(@JSName("$http") val underlying: HttpMissingBoundaryException) extends js.Object:
    import kyo.JsFacadeGivens.given
    def method() =
        underlying.method

    def url() =
        underlying.url


end JsHttpMissingBoundaryException