    // -------------------------------------------------------------------------
    // Cross-emitter consistency
    // -------------------------------------------------------------------------

    "all three emitters produce non-empty output for the same fixture" in {
        val jvmSrc    = JvmEmitter.emit(simpleSpec)
        val nativeSrc = NativeEmitter.emit(simpleSpec)
        val jsSrc     = JsEmitter.emit(simpleSpec)
        assert(jvmSrc.nonEmpty)
        assert(nativeSrc.nonEmpty)
        assert(jsSrc.nonEmpty)
    }

    "all three emitters produce syntactically valid Scala for the multi-method fixture" in {
        val fixtures = List(simpleSpec, multiMethodSpec, structSpec)
        for fixture <- fixtures do
            val jvmSrc    = JvmEmitter.emit(fixture)
            val nativeSrc = NativeEmitter.emit(fixture)
            val jsSrc     = JsEmitter.emit(fixture)

            val (jvmOk, jvmErrors)       = ScalaParseProbe.parses(jvmSrc)
            val (nativeOk, nativeErrors) = ScalaParseProbe.parses(nativeSrc)
            val (jsOk, jsErrors)         = ScalaParseProbe.parses(jsSrc)

            assert(jvmOk == true, s"JVM parse errors for ${fixture.simpleName}:\n$jvmErrors")
            assert(nativeOk == true, s"Native parse errors for ${fixture.simpleName}:\n$nativeErrors")
            assert(jsOk == true, s"JS parse errors for ${fixture.simpleName}:\n$jsErrors")
        end for
    }

    "all three emitters include the impl class name in their output" in {
        val fixtures = List(simpleSpec, multiMethodSpec, structSpec)
        for fixture <- fixtures do
            val implClassName = s"${fixture.simpleName}Impl"
            val jvmSrc        = JvmEmitter.emit(fixture)
            val nativeSrc     = NativeEmitter.emit(fixture)
            val jsSrc         = JsEmitter.emit(fixture)
            assert(jvmSrc.contains(implClassName), s"JVM missing impl class for ${fixture.simpleName}")
            assert(nativeSrc.contains(implClassName), s"Native missing impl class for ${fixture.simpleName}")
            assert(jsSrc.contains(implClassName), s"JS missing impl class for ${fixture.simpleName}")
        end for
    }

