package kyo.ffi.codegen.emitters

import EmitterFixtures.*
import kyo.ffi.codegen.model.*

/** Round-trip test: verifies that each emitter produces output that is
  *   1. Non-empty and structurally well-formed (contains expected Scala markers),
  *   2. Syntactically valid Scala 3 (as confirmed by ScalaParseProbe), and
  *   3. Consistently produced by all three emitters for a shared fixture.
  */
class EmitterRoundTripTest extends kyo.test.Test[Any]:

    // -------------------------------------------------------------------------
    // Shared fixtures
    // -------------------------------------------------------------------------

    private val simpleSpec = mkTrait(
        "Simple",
        "simple",
        List(
            mkMethod("noop", "noop", Nil, ReturnShape.Void)
        )
    )

    private val multiMethodSpec = mkTrait(
        "TcpBindings",
        "kyo_tcp",
        List(
            mkMethod(
                "tcpClose",
                "tcp_close",
                List(ParamSpec("fd", TypeRef.IntT)),
                ReturnShape.Void
            ),
            mkMethod(
                "tcpRead",
                "tcp_read",
                List(
                    ParamSpec("fd", TypeRef.IntT),
                    ParamSpec("buf", TypeRef.BufferT(TypeRef.ByteT)),
                    ParamSpec("maxLen", TypeRef.IntT)
                ),
                ReturnShape.Primitive(TypeRef.IntT),
                blocking = true
            ),
            mkMethod(
                "tcpWrite",
                "tcp_write",
                List(
                    ParamSpec("fd", TypeRef.IntT),
                    ParamSpec("data", TypeRef.ArrayT(TypeRef.ByteT)),
                    ParamSpec("len", TypeRef.IntT)
                ),
                ReturnShape.Primitive(TypeRef.IntT)
            )
        )
    )

    private val structSpec: TraitSpec =
        val cr = StructSpec(
            "kyo.example.ConnectResult",
            "ConnectResult",
            List(StructField("fd", TypeRef.IntT), StructField("pending", TypeRef.IntT)),
            packed = false
        )
        mkTrait(
            "TcpConnect",
            "kyo_tcp",
            List(
                mkMethod(
                    "tcpConnect",
                    "tcp_connect",
                    List(ParamSpec("host", TypeRef.StringT), ParamSpec("port", TypeRef.IntT)),
                    ReturnShape.MultiValue(cr),
                    blocking = true
                )
            ),
            structs = List(cr)
        )
    end structSpec

    // -------------------------------------------------------------------------
    // Helper: count opening vs closing braces
    // -------------------------------------------------------------------------

    private def hasBalancedBraces(src: String): Boolean =
        var depth = 0
        for ch <- src do
            if ch == '{' then depth += 1
            else if ch == '}' then depth -= 1
        depth == 0
    end hasBalancedBraces

    // -------------------------------------------------------------------------
    // JvmEmitter round-trip
    // -------------------------------------------------------------------------

    "JvmEmitter round-trip, simple spec" - {

        "output is non-empty" in {
            val src = JvmEmitter.emit(simpleSpec)
            assert(src.nonEmpty)
        }

        "output parses as valid Scala 3" in {
            val src            = JvmEmitter.emit(simpleSpec)
            val (ok, errorMsg) = ScalaParseProbe.parses(src)
            assert(ok == true, s"Parse errors:\n$errorMsg\n\nSource:\n$src\n")
        }

        "output contains the AUTO-GENERATED banner" in {
            val src = JvmEmitter.emit(simpleSpec)
            assert(src.startsWith("// AUTO-GENERATED by kyo-ffi-codegen"))
        }

        "output contains the package declaration" in {
            val src = JvmEmitter.emit(simpleSpec)
            assert(src.contains("package kyo.example"))
        }

        "output contains the impl class declaration" in {
            val src = JvmEmitter.emit(simpleSpec)
            assert(src.contains("class SimpleImpl"))
        }

        "output contains AbiCheck.verify" in {
            val src = JvmEmitter.emit(simpleSpec)
            assert(src.contains("AbiCheck.verify"))
        }

        "output contains NativeLoader.load" in {
            val src = JvmEmitter.emit(simpleSpec)
            assert(src.contains("NativeLoader.load"))
        }

        "output has balanced braces" in {
            val src = JvmEmitter.emit(simpleSpec)
            assert(hasBalancedBraces(src) == true)
        }
    }

    "JvmEmitter round-trip, multi-method spec" - {

        "output parses as valid Scala 3" in {
            val src            = JvmEmitter.emit(multiMethodSpec)
            val (ok, errorMsg) = ScalaParseProbe.parses(src)
            assert(ok == true, s"Parse errors:\n$errorMsg\n\nSource:\n$src\n")
        }

        "output contains impl class declaration" in {
            val src = JvmEmitter.emit(multiMethodSpec)
            assert(src.contains("class TcpBindingsImpl"))
        }

        "output contains def for each method" in {
            val src = JvmEmitter.emit(multiMethodSpec)
            assert(src.contains("def tcpClose"))
            assert(src.contains("def tcpRead"))
            assert(src.contains("def tcpWrite"))
        }

        "output has balanced braces" in {
            val src = JvmEmitter.emit(multiMethodSpec)
            assert(hasBalancedBraces(src) == true)
        }
    }

    "JvmEmitter round-trip, struct/multi-value spec" - {

